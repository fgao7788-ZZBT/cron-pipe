from pathlib import Path
from cron_pipe import StateWriter

STATE_FILE = str(Path.home() / "__CRON_PIPE_STATE_PATH__")


def evaluate() -> dict:
    """Your condition logic here. Keep it light (no LLM, no heavy APIs).

    Returns a dict with at least "action": "PROCEED" or "HALT".
    Include "score" (float) if you plan to use threshold gating.
    """
    # --- Replace below with your logic ---
    # Example: check if it's a specific time window
    import datetime
    now = datetime.datetime.now()
    is_active = 9 <= now.hour <= 15
    return {
        "action": "PROCEED" if is_active else "HALT",
        "score": 0.8 if is_active else 0.0,
    }


