"""cron-pipe CLI — ``cron-pipe init`` generates writer/reader boilerplate."""

import argparse
import os
import sys
from pathlib import Path

from . import __version__

_STATE_PATH_PLACEHOLDER = "__CRON_PIPE_STATE_PATH__"
_THRESHOLD_PLACEHOLDER = "__CRON_PIPE_THRESHOLD__"

